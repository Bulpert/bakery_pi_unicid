     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'produto' => 'required|',
            'peso' => 'required|',
            'tipo_peso' => 'required|',
            'valor' => 'required|',
            'imagem' => 'required|'
        ]);

            Produto::create($request->all());
            return redirect()->route('produtos.index');
    }

